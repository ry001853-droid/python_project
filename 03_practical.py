# write program using if-else and if-elif-else
a = 15

if a % 2 == 0:
   print(a, "is an even number.")
else:
   print(a, "is an odd number.")

# using if-elif-else 
age = 25

if age < 13:
   print("Child")   
elif age < 20:
   print("Teenager")        
elif age < 60:
   print("Adult")
else:
   print("Senior Citizen")


